package kz.lab.redisapp.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.redis.connection.ReactiveRedisConnectionFactory;
import org.springframework.data.redis.core.ReactiveRedisOperations;
import org.springframework.data.redis.core.ReactiveRedisTemplate;
import org.springframework.data.redis.serializer.JacksonJsonRedisSerializer;
import org.springframework.data.redis.serializer.RedisSerializationContext;
import org.springframework.data.redis.serializer.StringRedisSerializer;

import kz.lab.redisapp.model.ClientDataRecord;

@Configuration
public class ClientDataConfiguration {
    @Bean
    ReactiveRedisOperations<String, ClientDataRecord> redisOperations(ReactiveRedisConnectionFactory factory) {
        JacksonJsonRedisSerializer<ClientDataRecord> serializer = new JacksonJsonRedisSerializer<>(
                ClientDataRecord.class);

        RedisSerializationContext.RedisSerializationContextBuilder<String, ClientDataRecord> builder = RedisSerializationContext
                .newSerializationContext(new StringRedisSerializer());

        RedisSerializationContext<String, ClientDataRecord> context = builder.value(serializer).build();

        return new ReactiveRedisTemplate<>(factory, context);
    }
}
